public class Array {
    public static void main(String[]args)
    {
        char alphbet[]={'A','B','C','D','E','F'};
        System.out.println(alphbet[0]);
        // For printing all the array elements using for loop
        for(int i=0;i<alphbet.length;i++)
            {
                 System.out.println(alphbet[i]);
            }
    }
}
